package project;

public class Char {

	public static void main(String[] args) {
		char ch = 'b';
		System.out.println(ch);
		char ch1 = 23;
		System.out.println(ch1);
		char var1 = '\u2764';
		System.out.println(var1);
		char var2 = '\u0024';
		System.out.println(var2);
		int a = 'C';
		System.out.println(a);
	}

}
